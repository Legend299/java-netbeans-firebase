    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) tblListaEstudiantes.getModel();
        int regSeleccionado = tblListaEstudiantes.getSelectedRow();

        String id = dtm.getValueAt(regSeleccionado, 0).toString();
        Estudiante estudiante = new Estudiante();
        estudiante.setNombre(dtm.getValueAt(regSeleccionado, 1).toString());
        estudiante.setSemestre(Integer.valueOf(dtm.getValueAt(regSeleccionado, 2).toString()));
        estudiante.setCorreo(dtm.getValueAt(regSeleccionado, 3).toString());

        this.dispose();

        new JFModificarEstudiantes(estudiante, id).setVisible(true);
    }//GEN-LAST:event_btnModificarActionPerformed

