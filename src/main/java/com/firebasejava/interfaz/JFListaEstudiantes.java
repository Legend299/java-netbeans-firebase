    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        DefaultTableModel dtm = (DefaultTableModel) tblListaEstudiantes.getModel();
        int regSeleccionado = tblListaEstudiantes.getSelectedRow();

        int respuestaOptionPane = JOptionPane.showConfirmDialog(null, "Â¿Deseas eliminar este estudiante? \n ID: " + dtm.getValueAt(regSeleccionado, 0).toString()
                + "\n Nombre: " + dtm.getValueAt(regSeleccionado, 1).toString());

        if (JOptionPane.OK_OPTION == respuestaOptionPane) {
            estudianteDAO.EliminarEstudiante(dtm.getValueAt(regSeleccionado, 0).toString());
        }

        tblListaEstudiantes.setModel(estudianteDAO.ListarEstudiantes(tblListaEstudiantes));
        dtm.setRowCount(0);


    }//GEN-LAST:event_btnEliminarActionPerformed

